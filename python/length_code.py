"""

1. How to print the length of the string 'abcd' ?
2. How to print the length of the variable x (x is the list [5, 30 ,2]) ?
3. What would be the length of following dictionary {'x': 3, 'y': 3} ?
4. What would be the length of the tuple ('x', 'y') ?

"""

dummy_string = "abcd"
print("String Length:", len(dummy_string), sep=" ")

x = [5, 30, 2]
print("Variable Length:", len(x), sep=" ")

dummy_dictionary = {'x': 3, 'y': 3}
print("Dictionary Length:", len(dummy_dictionary), sep=" ")

dummy_tuple = ('x', 'y')
print("Tuple Length:", len(dummy_tuple), sep=" ")
